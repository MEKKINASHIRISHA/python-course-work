#3.DataTypes

#1.NumericTypes
a=10
print(type(a)) #<class 'int'>
a=10.0
print(type(a)) #<class 'float'>
a=3+4j
print(type(a)) #<class 'complex'>

#2.string
name='Shirisha'
print(type(name)) #<class 'str'>
name="Shirisha"
print(type(name)) #<class 'str'>
name='''Shirisha'''
print(name) #Shirisha
print(type(name)) #<class 'str'>
